#include "libft.h"

/* ft_isalpha: Checks if the character is alphabetic. */
int	ft_isalpha(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	return (0);
}
